package Javabeans;

/**
 *
 * @author h11hu
 */
public class Aviao {
    String prefixo_aeronautico;
    int idade;
    String modelo;
    int assentos;
    int capacidade_tanque;

    public String getPrefixo_aeronautico() {
        return prefixo_aeronautico;
    }

    public void setPrefixo_aeronautico(String prefixo_aeronautico) {
        this.prefixo_aeronautico = prefixo_aeronautico;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public int getAssentos() {
        return assentos;
    }

    public void setAssentos(int assentos) {
        this.assentos = assentos;
    }

    public int getCapacidade_tanque() {
        return capacidade_tanque;
    }

    public void setCapacidade_tanque(int capacidade_tanque) {
        this.capacidade_tanque = capacidade_tanque;
    }

  
}
